public class Customer {
  
  long custId;
  String fullName;
  String email;
  String phone;
  
  public Customer(long custId, String fullName, String email, String phone) {
    custId = this.custId;
    fullName = this.fullName;
    email = this.email;
    phone = this.phone;
  }
  
  
  
}
